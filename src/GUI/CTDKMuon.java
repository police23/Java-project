    private void HienThiCTDKMuon() {
        try {
            DKMuon dkmuon = new DKMuon();
            dkmuon.setMaDG(CurrentUser.getInstance().getMaND());

            DKMuonDAO dkMuonDAO = new DKMuonDAO();
            List<Book> sachDaDangKy = dkMuonDAO.getSachDaDK(dkmuon);
            DefaultTableModel model = (DefaultTableModel) tableCTDK.getModel();
            model.setRowCount(0);
            for (Book bk : sachDaDangKy) {
                model.addRow(new Object[] {bk.getMaSach(), bk.getTenSach()});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
