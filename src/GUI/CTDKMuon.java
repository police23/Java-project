    private void btnHuyDKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHuyDKActionPerformed
        String maDG = CurrentUser.getInstance().getMaND(); 

        int rowCount = tableCTDK.getRowCount();
        String[] maSachArray = new String[rowCount];
        for (int i = 0; i < rowCount; i++) {
            maSachArray[i] = (String) tableCTDK.getValueAt(i, 0); 
    }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn hủy đăng ký mượn tất cả sách này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
            dkMuonBUS.deleteDK(maDG, maSachArray);
            JOptionPane.showMessageDialog(this, "Hủy đăng ký mượn sách thành công!");
            DefaultTableModel model = (DefaultTableModel) tableCTDK.getModel();
            model.setRowCount(0);
            if (updateTable != null) {
                updateTable.updateTable();
    }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnHuyDKActionPerformed

