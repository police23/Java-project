    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String noti = "";
        System.out.println(Date_NgaySinh.getDate());
        if (txtHoVaTen.getText().equals("")) {
            noti += "Chưa nhập họ và tên\n";
        }
        if (Date_NgaySinh.getDate() == null) {
            noti += "Chưa chọn ngày sinh\n";
        }
        else if (!Pattern.matches("[\\p{L} ]+", txtHoVaTen.getText())) {
            noti += "Tên người dùng chỉ được chứa các ký tự chữ cái\n";
        }

        if (txtSoDT.getText().equals("")) {
            noti += "Chưa nhập số điện thoại\n";
        }
        else if (!Pattern.matches("\\d+", txtSoDT.getText())) {
            noti += "Số điện thoại chỉ được chứa các ký tự chữ số\n";
        } else if (txtSoDT.getText().length() > 11 || txtSoDT.getText().length() < 10) {
            noti += "Số điện thoại là số có 10 hoặc 11 chữ số\n";
        }
        if (!Pattern.matches(EMAIL_PATTERN, txt_Email.getText())) {
            noti += "Email không hợp lệ\n";
        }
        
        if (!noti.isEmpty()) {
            JOptionPane.showMessageDialog(this, noti, "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
        else {
            tk.setEmail(txt_Email.getText());
            tk.setGioiTinh(ComboBox_GioiTinh.getSelectedIndex());
            System.out.println(ComboBox_GioiTinh.getSelectedIndex());
            tk.setVaiTro(ComboBox_VaiTro.getSelectedIndex() + 1);
            System.out.println(ComboBox_VaiTro.getSelectedIndex() + 1);
            tk.setHoTen(txtHoVaTen.getText());
            tk.setNgaySinh(Date_NgaySinh.getDate());
            tk.setSoDT(txtSoDT.getText());
            tkb.addTaiKhoan(tk);
            qltk.updateTable();
            
        }
          this.dispose();  
        

    }//GEN-LAST:event_btnOKActionPerformed

