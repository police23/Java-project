    private void btn_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OKActionPerformed
        String userInputOTP = txtOTP.getText().trim();
        if (userInputOTP.equals(otp)) {
            // OTP verification successful
            JOptionPane.showMessageDialog(this, "Xác thực OTP thành công");
            // Thực hiện reset mật khẩu hoặc các hành động khác sau khi xác minh thành công
            dispose(); // Đóng cửa sổ InputOtp sau khi xác minh thành công
            ResetPass rep = new ResetPass(email);
            rep.setLocationRelativeTo(null);
            rep.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(this, "Xác thực OTP thất bại. Vui lòng thử lại");
        }
    }//GEN-LAST:event_btn_OKActionPerformed

